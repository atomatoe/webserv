#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long long int a;

	a = n;
	if (a < 0)
	{
		write(fd, "-", 1);
		a = a * -1;
	}
	if (a / 10)
		ft_putnbr_fd(a / 10, fd);
	n = a % 10 + '0';
	write(fd, &n, 1);
}
